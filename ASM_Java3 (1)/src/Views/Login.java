    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if (isValidate()) {
            String username = txtUsername.getText();
            String password = new String(txtPassword.getPassword());
            Account acc = AccountController.getAccount(username, password);
            if (acc != null) {
                CurrentLogin.name = acc.getUsername();
                CurrentLogin.role = acc.getRole();
                if (isRemember) {
                    AccountController.clearRememberAccount();
                    AccountController.rememberAccount(username);
                } else {
                    AccountController.clearRememberAccount();
                }
                if (acc.getRoleid() == 1) {
                    QLSV qlsv = new QLSV();
                    dispose();
                    qlsv.setVisible(true);
                } else {
                    QLD qld = new QLD();
                    dispose();
                    qld.setVisible(true);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Đăng nhập thất bại");
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

