    private void lblVisibleMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblVisibleMouseClicked
        if (isShowPass) {
            lblVisible.setIcon(new ImageIcon(".\\icons\\hide.png"));
            txtPassword.setEchoChar((char) 0);
        } else {
            txtPassword.setEchoChar('*');
            lblVisible.setIcon(new ImageIcon(".\\icons\\show.png"));
        }
        isShowPass = !isShowPass;
    }//GEN-LAST:event_lblVisibleMouseClicked

    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        chkRemember.setSelected(true);
        Account acc = AccountController.getRememberAccount();
        lblVisible.setIcon(new ImageIcon(".\\icons\\show.png"));
        if (acc != null) {
            txtUsername.setText(acc.getUsername());
            txtPassword.setText(acc.getPassword());
        }
    }//GEN-LAST:event_formWindowOpened

